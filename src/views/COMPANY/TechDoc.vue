      </p>
  
      <h3>2. Обработка транзакции</h3>
      <p>
        После получения транзакции система блокирует активы пользователя в исходной сети через смарт-контракт. В это время система начинает подготовку для выпуска эквивалентных активов в целевой сети, следуя механизмам кроссчейн-протоколов.
      </p>
  
      <h3>3. Использование zk-SNARK и миксера</h3>
      <p>
        Перед отправкой активов в целевую сеть, транзакция проходит через миксер, где она разбивается на части и перемешивается с другими транзакциями. zk-SNARK в этот момент обеспечивает верификацию, что транзакция подлинна, без раскрытия её деталей.
      </p>
  
      <h3>4. Кроссчейн-трансфер</h3>
      <p>
        После прохождения миксинга, Bridge<span class="text-danger">X</span> генерирует эквивалентные активы в целевой сети, где они становятся доступны для пользователя. Эти активы уже не связаны с оригинальной транзакцией, обеспечивая полную анонимность.
      </p>
  
      <h3>5. Подтверждение транзакции</h3>
      <p>
        Пользователь получает уведомление о завершении трансфера, а транзакция отображается в обеих сетях — в исходной и целевой, при этом обеспечивается полная анонимность.
      </p>
  
