      <h2 id="velos-workflow">Процесс работы системы Bridge<span class="text-danger">X</span></h2>
      <p>
        Bridge<span class="text-danger">X</span> предоставляет пользователям единый интерфейс для отправки активов в любые блокчейн-сети. Процесс работы системы можно разделить на несколько этапов:
